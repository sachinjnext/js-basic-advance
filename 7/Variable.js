// 4 Ways to Declare a JavaScript Variable:
// Using var
var x = 5;
var y = 6;
var z = x + y;
console.log(z);

// Using let
let a = 9;
let b = 6;
let c = a + b;
console.log(c);

// Using const
const price1 = 5;
const price2 = 10; // prefix value
let total = price1 + price2; // change value
console.log(total);

// Using nothing
p = 5;
q = 16;
r = p + q;
console.log(r);